<h2 style="color:white">Impact of this project:</h2>
</div>
<p style="color:black;font-size:20px;">
In our country, if farmers face any kinds of problems regarding their crops, they do not understand where to contact. Our project will help them to contact agriculturists or veterinary specialists. Sometimes farmers need money to cultivate but they find it difficult to manage it. Even if they can hardly manage loans, they have to pay a huge amount of interest. Our project will help them to take loans and microcredits with a negligible amount of interest. Usually, the farmers sell their products to wholesalers and they sell them to the consumers. As a result, sometimes it is seen that farmers are not getting the proper price as the market price. Even the customers also want farm-fresh food which they sometimes may not get in this process. Our project will allow the customers to buy products directly from the farmers so that the farmers get proper money and the buyers get fresh food.  
</p>

