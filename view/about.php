<h2 style="color:white">Motivation:</h2>
</div>
<img src="../view/nobanno.jpg" height=300 style="float:right;" >
<p style="color:black;font-size:20px;">

Bangladesh is an agricultural country where 87 percent of rural households rely on agriculture for at least part of their income. 
But the agriculture of our country still lags behind. 
Because in our country farmers are not aware of the intelligent technologies much compared to farmers of other countries. 
As a result, if they face any kind of problem, they do not understand what to do or where to go. Sometimes they have to go a long distance to consult. 
At the same time, the farmers do not get the proper money for their products as per the original market price. As our country is getting digital and it is the manifesto of the present government to make a digital country, 
we wanted to make a web application for the farmers as smart devices are very much available now in the rural areas also. 
This web application might be a one-stop digital solution for farming problems.</p><br><br><br><br>

